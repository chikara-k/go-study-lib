package leetcode

import "sort"

func threeSum(nums []int) [][]int {
	sort.Ints(nums)
	n := len(nums)

	res := make([][]int, 0)

	for i := 0; i < n-2; i++ {
		// i の重複をスキップ
		if i > 0 && nums[i] == nums[i-1] {
			continue
		}
		// 先頭が正ならこれ以降は0を作れない
		if nums[i] > 0 {
			break
		}

		l, r := i+1, n-1
		for l < r {
			sum := nums[i] + nums[l] + nums[r]
			if sum == 0 {
				res = append(res, []int{nums[i], nums[l], nums[r]})
				l++
				r--

				// l 側の重複をスキップ
				for l < r && nums[l] == nums[l-1] {
					l++
				}
				// r 側の重複をスキップ
				for l < r && nums[r] == nums[r+1] {
					r--
				}
			} else if sum < 0 {
				l++
			} else {
				r--
			}
		}
	}

	return res
}
